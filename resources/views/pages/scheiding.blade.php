                        <div class="text-2 dark">
                            <p>Uit elkaar gaan is niet prettig, maar de manier waarop dat gebeurt kan voor beiden emotioneel een groot verschil maken. De zogenaamde vechtscheiding is een treurige aangelegenheid, omdat feitelijk beide partners enkel nog voor de eigen belangen en daar beiden ook nog eens de hoofdprijs voor betalen met advocaten. De fijne, vriendelijke oplossing bij het regelen van een scheiding is een mediator. De mediator dient de belangen van beiden en zoekt naar een eindoplossing die voor jullie beiden bevredigend is.</p>

                            <h3 class="heading-3 dark">Afspraken vastleggen</h3>

                            <p>De rol van de mediator bij het begeleiden van de scheiding is goed in kaart brengen wat jullie beiden wensen. De mediator zoekt (in overleg met beide partijen) naar compromissen. Als een akkoord bereikt wordt, dan worden de afspraken vastgelegd in een zogenaamd echtscheidingsconvenant. Beide partners ondertekenen het document, waarmee beide partijen de afspraken op schrift hebben staan. Dit is de kortste en vriendelijkste route naar een officiële afsluiting van de relatie.</p>

                            <p>Bij scheidingsmediation komen verschillende zaken aan bod. De verdeling van goederen en financiële middelen, de mogelijke <a href="https://www.huisverkopen.nl/?r=scheiding" target="_blank" style="color: #0b7eb8; text-decoration: underline;">verkoop van het huis</a>, een alimentatieplan en (indien van toepassing) een ouderschapsplan. Op alle punten moet een akkoord bereikt worden. De mediator speelt een centrale rol om te onderzoeken waar de wensen hetzelfde zijn en waar de wensen verschillend zijn. Bij onenigheden (en/of verschillende zienswijzen) zoekt de scheidingsmediator naar een passende oplossing voor beiden.</p>

                            <h3 class="heading-3 dark">Hoe gaat mediation bij scheiding in zijn werk?</h3>

                            <p>In eerste instantie inventariseert de mediator jullie huidige situatie. Hoe staan jullie er nu voor? Op welke punten zijn er onenigheden? Wat is voor jullie de gewenste uitkomst? De mediator maakt een inschatting van het aantal sessies dat nodig zal zijn om nader tot elkaar te komen. Als jullie beiden akkoord zijn om van start te gaan met mediation, dan worden de sessies ingepland en wordt bij het tweede gesprek een inhoudelijke start gemaakt.</p>

                            <h3 class="heading-3 dark">Bereidheid tot mediation is van belang</h3>

                            <p>Mediation bij scheiding kan alleen werken als beide partijen bereid zijn om te participeren in het traject. Het doel is immers om tot een akkoord te komen. Afspraken kunnen niet eenzijdig gemaakt worden. Daarom is een ingangseis dat beide partijen bereid zijn om mee te werken. Als mediation geen optie is omdat een partner niet wenst mee te werken, dan is een oplossing met advocaten helaas de enige route. Een advocaat dient dan jouw eigen belangen en heeft als doel om via de rechter jouw gewenste verdelingen af te dwingen. Echter, dit is altijd een duur traject.</p>

                            <h3 class="heading-3 dark">Doel is een goede oplossing</h3>

                            <p>Bij een scheiding zijn er een groot aantal knopen door te hakken met betrekking tot de verdeling. Emoties kunnen daarbij een dermate grote rol spelen, dat objectief een oplossing zoeken voor partners soms moeilijk is. De mediator ondersteunt jullie beiden op dit vlak. Voor de mediator is er maar één doel, namelijk dat jullie het beiden eens worden op een manier waar jullie beiden tevreden over zijn. Bij een eerste gesprek wordt dit benadrukt: de mediator is er voor jullie allebei bij het regelen van de scheiding.</p>
